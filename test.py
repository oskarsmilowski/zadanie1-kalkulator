def dodaj() -> float:
    print('Podaj pierwszą liczbę:')
    a = float(input())
    print('Podaj drugą liczbę: ')
    b = float(input())
    return a+b

def odejmij() -> float:
    print('Podaj liczbę od której chcesz odjąć')
    a = float(input())
    print('Podaj liczbę odejmowaną')
    b = float(input())
    return a-b
